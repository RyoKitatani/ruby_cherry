##  _ && _ かつ
t1 = true
t2 = true
f1 = false
p t1 && t2 
p t1 && f1 
p "--------------"
##  _ || _ または
t1 = true
f1 = false
f2 = false
p t1 || f1
p f1 || f2
p "--------------"
# && と ||を組み合わせることも可能
t1 = true
t2 = true
f1 = false
f2 = false
p t1 && t2 || f1 && f2
p "--------------"
# 優先順位を変えたい場合は、()を使用
p t1 && (t2 || f1) && f2
p "--------------"
# !_で真偽値を反転できる
p !t1
p !f1
p "--------------"
